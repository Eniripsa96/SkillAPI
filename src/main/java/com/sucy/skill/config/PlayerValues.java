package com.sucy.skill.config;

/**
 * <p>Config keys for player data values</p>
 * <p>This is primarily for the API saving data. You shouldn't need
 * to use these values at all.</p>
 */
public class PlayerValues {

    public static final String

    /**
     * Root path for player data
     */
    ROOT = "players",

    /**
     * Class level
     */
    LEVEL = "level",

    /**
     * Class experience
     */
    EXP = "exp",

    /**
     * Class name
     */
    CLASS = "class",

    /**
     * Skill progression
     */
    SKILLS = "skills",

    /**
     * Skill points
     */
    POINTS = "points",

    /**
     * Mana
     */
    MANA = "mana",

    /**
     * Player values
     */
    VALUES = "values",

    /**
     * Skill bindings
     */
    BIND = "binds";
}
